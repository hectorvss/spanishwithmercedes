function getLang() {
  return localStorage.getItem('slwm_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('slwm_lang', lang);
  applyLang(lang);
}

function applyLang(lang) {
  // Update body class for CSS show/hide (use 'is-spanish', NOT 'lang-es', to avoid self-hiding)
  document.body.classList.toggle('is-spanish', lang === 'es');

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = translations[lang][key];
    if (text !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
      } else if (text.includes('\n')) {
        el.innerHTML = text.split('\n').join('<br>');
      } else if (text.includes('<em>')) {
        el.innerHTML = text;
      } else {
        el.textContent = text;
      }
    }
  });

  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function initI18n() {
  const lang = getLang();

  // Attach language button listeners
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.getAttribute('data-lang')));
  });

  applyLang(lang);
}

document.addEventListener('DOMContentLoaded', initI18n);
