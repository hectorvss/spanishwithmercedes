function getLang() {
  return localStorage.getItem('slwm_lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('slwm_lang', lang);
  applyLang(lang);
}

function applyLang(lang) {
  // Update body class for CSS show/hide (use 'is-spanish', NOT 'lang-es', to avoid self-hiding)
  document.body.classList.toggle('is-spanish', lang === 'es');

  // Update all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const text = translations[lang][key];
    if (text !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = text;
      } else if (text.includes('\n')) {
        el.innerHTML = text.split('\n').join('<br>');
      } else if (text.includes('<em>')) {
        el.innerHTML = text;
      } else {
        el.textContent = text;
      }
    }
  });

  // Update lang buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
  });
}

function initI18n() {
  const lang = getLang();

  // Attach language button listeners
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.getAttribute('data-lang')));
  });

  applyLang(lang);
}

document.addEventListener('DOMContentLoaded', initI18n);

// ── Mobile hamburger menu ──────────────────────────────────
function initMobileMenu() {
  const hamburger = document.getElementById('nav-hamburger');
  if (!hamburger) return;

  const nav = document.querySelector('.header__nav');
  if (!nav) return;

  const mobileMenu = document.createElement('div');
  mobileMenu.className = 'mobile-menu';
  mobileMenu.id = 'mobile-menu';

  nav.querySelectorAll('.nav-item-dropdown').forEach(item => {
    const trigger = item.querySelector('.nav-item-trigger');
    if (!trigger) return;
    const link = document.createElement('a');
    link.href = trigger.href || '#';
    link.innerHTML = trigger.innerHTML;
    const i18nKey = trigger.getAttribute('data-i18n');
    if (i18nKey) link.setAttribute('data-i18n', i18nKey);
    mobileMenu.appendChild(link);

    const dropdown = item.querySelector('.nav-dropdown');
    if (dropdown) {
      dropdown.querySelectorAll('a').forEach(subA => {
        const sub = subA.cloneNode(true);
        sub.classList.add('mobile-menu__sub');
        mobileMenu.appendChild(sub);
      });
    }
  });

  const subscribeBtn = document.querySelector('.header__subscribe');
  if (subscribeBtn) {
    const sub = subscribeBtn.cloneNode(true);
    sub.style.cssText = 'background:#121117;color:#fff;border-color:#121117;margin-top:8px;display:block;text-align:center;';
    mobileMenu.appendChild(sub);
  }

  document.body.appendChild(mobileMenu);

  const header = document.querySelector('.header');
  function positionMenu() {
    if (!header) return;
    const rect = header.getBoundingClientRect();
    // Use rect.bottom when header is in viewport, otherwise fall back to offsetHeight
    const top = (rect.bottom > 0 && rect.bottom < window.innerHeight) ? rect.bottom : header.offsetHeight;
    mobileMenu.style.top = top + 'px';
  }

  // Keep menu position updated as user scrolls (in case menu is open)
  window.addEventListener('scroll', () => {
    if (mobileMenu.classList.contains('open')) positionMenu();
  }, { passive: true });

  hamburger.addEventListener('click', () => {
    hamburger.classList.toggle('open');
    positionMenu();
    mobileMenu.classList.toggle('open');
    document.body.style.overflow = mobileMenu.classList.contains('open') ? 'hidden' : '';
  });

  mobileMenu.querySelectorAll('a').forEach(a => {
    a.addEventListener('click', () => {
      hamburger.classList.remove('open');
      mobileMenu.classList.remove('open');
      document.body.style.overflow = '';
    });
  });
}

document.addEventListener('DOMContentLoaded', initMobileMenu);
